    public OportunidadesJDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
    
        OfertaDAO ofertadao = new OfertaDAO();
        List<Oferta> ofertasLista = ofertadao.obtenerTodasLasOfertas();
        msgOfertasLabel.setText("Encuentra tu próxima oportunidad profesional entre " + ofertasLista.size() + " ofertas activas");
    
        JPanel ofertasPanel = new JPanel(new GridLayout(0, 2, 15, 15));
        ofertasPanel.setBackground(new Color(240, 240, 240));
    
        for (Oferta oferta : ofertasLista) {
            OfertaPanel ofertaPanel = new OfertaPanel(oferta);
            ofertasPanel.add(ofertaPanel);
        }
    
        JScrollPane scrollPane = new JScrollPane(ofertasPanel);
        scrollPane.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED);
        scrollPane.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_NEVER);
        scrollPane.getVerticalScrollBar().setUnitIncrement(16);
    
        getContentPane().setLayout(new BorderLayout());
    
        JPanel headerPanel = new JPanel();
        headerPanel.setLayout(new BorderLayout());
        headerPanel.add(jLabel1, BorderLayout.NORTH);
        headerPanel.add(msgOfertasLabel, BorderLayout.SOUTH);
        headerPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
    
        getContentPane().add(headerPanel, BorderLayout.NORTH);
        getContentPane().add(scrollPane, BorderLayout.CENTER);
    
        pack();
    
        setSize(900, 650);
        setResizable(false);
    
        setLocationRelativeTo(parent);
    }

